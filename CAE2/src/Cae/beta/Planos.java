package Cae.beta;

public class Planos {

	private Long codPlano;
	private String tipoPlano;
	private String duracaoPlano;
	private String valor;
	
	
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	public Long getCodPlano() {
		return codPlano;
	}
	public void setCodPlano(Long codPlano) {
		this.codPlano = codPlano;
	}
	public String getTipoPlano() {
		return tipoPlano;
	}
	public void setTipoPlano(String tipoPlano) {
		this.tipoPlano = tipoPlano;
	}
	public String getDuracaoPlano() {
		return duracaoPlano;
	}
	public void setDuracaoPlano(String duracaoPlano) {
		this.duracaoPlano = duracaoPlano;
	}
		
	
}
