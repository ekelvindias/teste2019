package Cae.beta;

public class FunCS {

	
	private Long idFCS;
	private String nomeFCS;
	private String profissão;
	private String empresaS;

	public Long getIdFCS(){
		return idFCS;
	}
	public void setIdFCS(Long idFCS) {
		this.idFCS = idFCS;
	}
	public String getNomeFCS() {
		return nomeFCS;
	}
	public void setNomeFCS(String nomeFCS) {
		this.nomeFCS = nomeFCS;
	}
	public String getProfissão() {
		return profissão;
	}
	public void setProfissão(String profissão) {
		this.profissão = profissão;
	}
	public String getEmpresaS() {
		return empresaS;
	}
	public void setEmpresaS(String empresaS) {
		this.empresaS = empresaS;
	}
	
	
}
